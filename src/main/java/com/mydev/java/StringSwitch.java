package com.mydev.java;

/**
 * StringSwitch
 *
 */
public class StringSwitch {
	public static void main(String[] args) {
		String test = "Medium";

		switch (test) {
		case "Low":
			System.out.println("Low");
			break;
		case "Medium":
			System.out.println("Medium");
			break;
		case "High":
			System.out.println("High");
			break;
		}
	}
}
